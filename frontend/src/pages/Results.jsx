import React, { useState, useEffect } from 'react';
import axios from 'axios';
import { jsPDF } from 'jspdf';
import 'jspdf-autotable';
import { Search, Filter, Download, FileCheck, Info, Award, BarChart3, FileText } from 'lucide-react';

const Results = ({ user }) => {
  const [results, setResults] = useState([]);
  const [searchTerm, setSearchTerm] = useState("");
  const [filterStatus, setFilterStatus] = useState("all");
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchResults = async () => {
      try {
        const res = await axios.get(`http://localhost:5000/api/student-results/${user.id}`);
        setResults(res.data);
      } catch (err) {
        console.error("Error fetching results", err);
      } finally {
        setLoading(false);
      }
    };
    fetchResults();
  }, [user.id]);

  // NEW: Download the professional PDF generated by the backend
  const handleDownloadOfficial = (submissionId) => {
    if (!submissionId) {
      alert("Evaluation record not found.");
      return;
    }
    // Triggers the download from the Flask route you created
    window.open(`http://localhost:5000/api/download-report/${submissionId}`, '_blank');
  };

  // Filtering Logic
  const filteredResults = results.filter(res => {
    const matchesSearch = res.exam_title.toLowerCase().includes(searchTerm.toLowerCase());
    const matchesStatus = filterStatus === "all" || res.status === filterStatus;
    return matchesSearch && matchesStatus;
  });

  // Statistics Calculation
  const evaluatedExams = results.filter(r => r.status === 'evaluated');
  const avgMarks = evaluatedExams.length > 0 
    ? (evaluatedExams.reduce((acc, curr) => acc + curr.marks, 0) / evaluatedExams.length).toFixed(1) 
    : "0.0";

  // PDF Export Logic (Local Frontend Transcript)
  const downloadTranscript = () => {
    const doc = new jsPDF();
    doc.setFontSize(22);
    doc.setTextColor(26, 28, 75); 
    doc.text("OFFICIAL ACADEMIC TRANSCRIPT", 105, 20, { align: "center" });
    
    doc.setFontSize(10);
    doc.setTextColor(100);
    doc.text(`Student: ${user.name}`, 14, 35);
    doc.text(`Roll Number: ${user.roll_number || 'N/A'}`, 14, 40);
    doc.text(`Generated On: ${new Date().toLocaleString()}`, 14, 45);

    const tableRows = evaluatedExams.map(res => [
      res.exam_title,
      res.subject || 'General',
      res.marks + "%",
      res.feedback
    ]);

    doc.autoTable({
      startY: 55,
      head: [['Examination', 'Subject', 'Marks', 'AI Evaluation Feedback']],
      body: tableRows,
      theme: 'grid',
      headStyles: { fillColor: [26, 28, 75], fontStyle: 'bold' },
      styles: { fontSize: 9 }
    });

    doc.save(`${user.name}_Transcript.pdf`);
  };

  if (loading) return <div className="p-10 text-slate-400 font-bold animate-pulse">Loading Audit Logs...</div>;

  return (
    <div className="space-y-8 animate-in fade-in duration-700">
      
      {/* PERFORMANCE KPI HEADER */}
      <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
        <div className="bg-[#1A1C4B] p-8 rounded-[32px] text-white flex justify-between items-center overflow-hidden relative shadow-xl shadow-blue-900/20">
          <div>
            <p className="text-[10px] font-black opacity-60 uppercase tracking-[0.2em] mb-2">Mean Proficiency</p>
            <h3 className="text-4xl font-black">{avgMarks}%</h3>
          </div>
          <Award size={48} className="opacity-20 absolute -right-2 -bottom-2" />
        </div>

        <div className="bg-blue-600 p-8 rounded-[32px] text-white flex justify-between items-center overflow-hidden relative shadow-xl shadow-blue-600/20">
          <div>
            <p className="text-[10px] font-black opacity-60 uppercase tracking-[0.2em] mb-2">Evaluations Synced</p>
            <h3 className="text-4xl font-black">{evaluatedExams.length}</h3>
          </div>
          <FileCheck size={48} className="opacity-20 absolute -right-2 -bottom-2" />
        </div>

        <div className="bg-white border border-slate-100 p-8 rounded-[32px] flex flex-col justify-center shadow-sm">
          <button 
            onClick={downloadTranscript}
            className="flex items-center justify-center gap-3 bg-slate-900 text-white py-4 rounded-2xl font-black text-xs hover:bg-blue-600 transition shadow-xl shadow-slate-100"
          >
            <Download size={16} /> GENERATE TRANSCRIPT
          </button>
        </div>
      </div>

      {/* FILTERING BAR */}
      <div className="flex flex-col md:flex-row gap-4 items-center justify-between bg-slate-50 p-4 rounded-[24px]">
        <div className="relative flex-1 w-full">
          <Search className="absolute left-4 top-1/2 -translate-y-1/2 text-slate-400" size={18} />
          <input 
            type="text" 
            placeholder="Search Examination Audit..." 
            className="w-full pl-12 pr-4 py-3 bg-white border-none rounded-xl text-sm focus:ring-2 focus:ring-blue-500 shadow-sm"
            onChange={(e) => setSearchTerm(e.target.value)}
          />
        </div>
        <select 
          className="w-full md:w-48 p-3 bg-white border-none rounded-xl text-sm font-bold text-slate-600 shadow-sm"
          onChange={(e) => setFilterStatus(e.target.value)}
        >
          <option value="all">All Records</option>
          <option value="evaluated">Evaluated</option>
          <option value="pending">Pending</option>
        </select>
      </div>

      {/* RESULTS GRID */}
      <div className="space-y-4">
        {filteredResults.length > 0 ? (
          filteredResults.map(res => (
            <div key={res.id} className="group bg-white p-6 rounded-[28px] border border-slate-100 flex flex-col md:flex-row justify-between items-start md:items-center gap-6 hover:shadow-lg hover:border-blue-100 transition-all">
              <div className="flex-1">
                <div className="flex items-center gap-2 mb-1">
                  <span className={`px-2 py-0.5 rounded text-[8px] font-black uppercase tracking-widest ${
                    res.status === 'evaluated' ? 'bg-emerald-100 text-emerald-600' : 'bg-amber-100 text-amber-600'
                  }`}>
                    {res.status}
                  </span>
                  <span className="text-[10px] font-bold text-slate-400">SUBMISSION ID: #00{res.id}</span>
                </div>
                <h3 className="text-xl font-black text-slate-800 tracking-tight">{res.exam_title}</h3>
                
                {res.status === 'evaluated' ? (
                  <div className="mt-3 flex gap-3 items-start bg-slate-50 p-4 rounded-2xl border-l-4 border-blue-500">
                    <Info size={16} className="text-blue-500 shrink-0 mt-0.5" />
                    <p className="text-xs text-slate-600 font-medium italic leading-relaxed">
                      {res.feedback}
                    </p>
                  </div>
                ) : (
                  <p className="mt-2 text-xs font-bold text-slate-400 italic">Script currently being processed by AI Evaluation Engine...</p>
                )}
              </div>

              <div className="flex flex-col items-end gap-3 min-w-[140px]">
                <div className="bg-slate-50 px-6 py-4 rounded-2xl border border-slate-100 text-center w-full">
                  <p className="text-[10px] font-black text-slate-400 uppercase mb-1 tracking-tighter">Awarded Score</p>
                  <p className={`text-3xl font-black ${res.status === 'evaluated' ? 'text-blue-600' : 'text-slate-300'}`}>
                    {res.status === 'evaluated' ? `${res.marks}%` : '--'}
                  </p>
                </div>

                {/* OFFICIAL BACKEND PDF DOWNLOAD BUTTON */}
                {res.status === 'evaluated' && (
                  <button 
                    onClick={() => handleDownloadOfficial(res.id)}
                    className="flex items-center justify-center gap-2 w-full py-2 px-3 bg-blue-50 text-blue-700 rounded-xl text-[10px] font-black hover:bg-blue-600 hover:text-white transition-all uppercase"
                  >
                    <FileText size={14} /> Official Marksheet
                  </button>
                )}
              </div>
            </div>
          ))
        ) : (
          <div className="py-20 text-center">
            <BarChart3 size={48} className="mx-auto text-slate-200 mb-4" />
            <p className="text-slate-400 font-bold uppercase tracking-widest">No matching records found</p>
          </div>
        )}
      </div>
    </div>
  );
};

export default Results;